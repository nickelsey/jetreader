#include "gtest/gtest.h"

#include "jetreader/reader/bemc_helper.h"
#include <iostream>

// BemcRef lookup table for testing
class BemcRef {

public:
  // Default Constructor
  BemcRef();
  // destructor
  ~BemcRef() {}

  double getEta(unsigned tow_id);
  double getPhi(unsigned tow_id);

protected:
  float mTowGeom[4800][2];
};

TEST(BemcHelper, GetPhi) {
  jetreader::BemcHelper helper;
  BemcRef ref;
  for (int i = 1; i <= 4800; ++i) {
    EXPECT_NEAR(ref.getPhi(i), helper.towerPhi(i), 1e-4);
  }
}

TEST(BemcHelper, GetEta) {
  jetreader::BemcHelper helper;
  BemcRef ref;
  for (int i = 1; i <= 4800; ++i) {
    EXPECT_NEAR(ref.getEta(i), helper.towerEta(i), 1e-4);
  }
}

